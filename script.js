//var for clear the interval
let intervalCount = null;
// pause and restart the timer;
let pausedCounter = false;
//var to check if the typing is start of not,
let StartTyping = false;
//for stop the timer auto if the user stop typing for 8s.
let autoTimerStop = null;
// start timer function call when the typing is start,press the start btn.
function startCounting() {
  let avtiveTimeElem = document.querySelector(
    ".test-contents .timerValues .time.active"
  );
  intervalCount = window.setInterval(() => {
    testTimer -= 1;
    avtiveTimeElem.innerHTML = testTimer;
    //restart the test if the timer end
    if (testTimer === 0) {
      window.clearInterval(testTimer);
      showResult();
    }
  }, 1000);
  pauseCounterBtn.className = "fa-solid fa-pause pause-counter-btn";
  pausedCounter = false;
  StartTyping = true;
  window.clearTimeout(autoTimerStop);
  autoTimerStop = window.setTimeout(stopCounting, 8000);
}
// pause timer function call when press the pause btn
function stopCounting() {
  pauseCounterBtn.className = "fa-solid fa-play pause-counter-btn";
  pausedCounter = true;
  window.clearInterval(intervalCount);
  //to start the counter again if the typing is start.
  StartTyping = false;
}
pauseCounterBtn.addEventListener("click", () => {
  if (!pausedCounter) {
    stopCounting();
  } else {
    startCounting();
  }
});
