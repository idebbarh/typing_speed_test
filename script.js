function restartBtnFunc(rep) {
  let avtiveTimeElem = document.querySelector(
    ".test-contents .timerValues .time.active"
  );
  let inactiveTimers = document.querySelectorAll(
    ".test-contents .timerValues .time.inactive"
  );
  //resit the mistake and cpm counters.
  mistakesCounter = cpmCounter = 0;
  //hide the pause btn and make the restart btn to center;
  pauseCounterBtn.style.display = "none";
  pauseCounterBtn.className = "fa-solid fa-pause pause-counter-btn";
  pausedCounter = false;
  //clear the interval and restart the value of the timer
  window.clearInterval(intervalCount);
  testTimer = parseInt(avtiveTimeElem.getAttribute("data-time"));
  avtiveTimeElem.innerHTML = testTimer;
  //to start the counter again if the typing is start.
  StartTyping = false;
  //remove the prev text
  testParg.innerHTML = "";
  //show all inactive timers.
  inactiveTimers.forEach((timer) => {
    timer.style.display = "block";
  });
  // if user click the repeat btn the rep will be true, then we pass false to generateText() for generate the same paragraph.
  rep === true ? generateText(false) : generateText(true);
}
