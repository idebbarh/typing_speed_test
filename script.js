window.addEventListener("keydown", (e) => {
  //testStart true = (showing the test page), false = (showing the result page);
  if (testStart) {
    let activeChar = document.querySelector(
      ".test-contents .test-parag .word.active .character.active"
    );
    let activeWord = document.querySelector(
      ".test-contents .test-parag .word.active"
    );
    function backToPrevWord() {
      //remove the active from the active char
      activeChar.classList.remove("active");
      //remove the active from the active word
      activeWord.classList.remove("active");
      //add class active to the prev word.
      activeWord.previousElementSibling.classList.add("active");
      // remove the incorrect class form the prev elem word.
      activeWord.previousElementSibling.classList.remove("incorrect");
      activeWord.previousElementSibling.classList.remove("correct");
      //add class active to the last char in the prev elem word.
      activeWord.previousElementSibling
        .querySelector(".character:last-child")
        .classList.add("active");
      //check if the prev char incorrect
      if (
        activeWord.previousElementSibling
          .querySelector(".character:last-child")
          .classList.contains("incorrect")
      ) {
        //remove the incorrect class from prev char elem
        activeWord.previousElementSibling
          .querySelector(".character:last-child")
          .classList.remove("incorrect");
        mistakesCounter -= 1;
        //check if the prev char correct
      } else {
        //remove the correct class from prev char elem
        activeWord.previousElementSibling
          .querySelector(".character:last-child")
          .classList.remove("correct");
        if (
          !activeWord.previousElementSibling
            .querySelector(".character:last-child")
            .classList.contains("white-space")
        ) {
          cpmCounter -= 1;
        }
      }
    }
    function moveClassActiveToPrevElem() {
      //check if an prev Elem
      if (activeChar.previousElementSibling !== null) {
        // start the time interval one time when the typing is
        if (!StartTyping) {
          startCounting();
        }
        //remove the active from the active char
        activeChar.classList.remove("active");
        //add active to prev char elem
        activeChar.previousElementSibling.classList.add("active");
        //check if the prev char incorrect
        if (activeChar.previousElementSibling.classList.contains("incorrect")) {
          //remove the incorrect class from prev char elem
          activeChar.previousElementSibling.classList.remove("incorrect");
          mistakesCounter -= 1;
          //check if the prev char correct
        } else {
          //remove the correct class from prev char elem
          activeChar.previousElementSibling.classList.remove("correct");
          cpmCounter -= 1;
        }
      } else {
        //if not prev char elem, check if an prev word and the word in incorrect.
        if (activeWord.previousElementSibling !== null) {
          // start the time interval one time when the typing is
          if (!StartTyping) {
            startCounting();
          }
          //check if the active char not white space
          if (!activeChar.classList.contains("white-space")) {
            //if not white space check if prev prev word elem is incorrect or prev elem is incorrect.
            if (
              activeWord.previousElementSibling.previousElementSibling.classList.contains(
                "incorrect"
              ) ||
              activeWord.previousElementSibling.classList.contains("incorrect")
            ) {
              backToPrevWord();
            }
          } else {
            //if white space check the prev word elem is incorrect.
            if (
              activeWord.previousElementSibling.classList.contains("incorrect")
            ) {
              backToPrevWord();
            }
          }
        }
      }
    }
    //call the function when the key is backspace.
    if (e.key === "Backspace") {
      moveClassActiveToPrevElem();
    }
  }
});
