//save the paragraph for uset if the user choose the repeat the same test.
let prevParagraph = null;
function generateText(newParagraph) {
  let paragraf = null;
  //if we pass true to the main function,//make a random index to choose random pargraphs from the array of pargraphs.
  if (newParagraph) {
    paragraf = paragraphs[Math.trunc(Math.random() * paragraphs.length)];
    //if we pass false , we generate the prev paragraph who saved in the (prevParagraph) var.
  } else {
    paragraf = prevParagraph;
  }
  //save paragraph
  prevParagraph = paragraf;
  //split the paragraph word by word
  let words = paragraf.split(" ");
  //loop in all the words
  words.forEach((word) => {
    //add a new div class word
    testParg.innerHTML += `<div class="word"></div>`;
    //loop in all the character in the word
    word.split("").forEach((char) => {
      //add earch character in specific span
      testParg.querySelector(
        ".word:last-child"
      ).innerHTML += `<span class="character">${char}</span>`;
    });
    //add white space
    testParg.innerHTML += `<div class="word space"><span class="character white-space">&nbsp;</span></div>`;
  });

  // add class active to the first word and the first char in the first word
  let firstWord = document.querySelector(
    ".test-contents .test-parag .word:first-child"
  );
  let firstChar = firstWord.querySelector(".character:first-child");
  firstWord.classList.add("active");
  firstChar.classList.add("active");
}
